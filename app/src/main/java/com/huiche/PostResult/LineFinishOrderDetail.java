package com.huiche.PostResult;

public class LineFinishOrderDetail {
	public String[] goodsThumbs;
	public String name;
	public String count;
	public String marketPrice;
	public String integral;

	public LineFinishOrderDetail() {

	}

	public String[] getGoodsThumbs() {
		return goodsThumbs;
	}

	public void setGoodsThumbs(String[] goodsThumbs) {
		this.goodsThumbs = goodsThumbs;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCount() {
		return count;
	}

	public void setCount(String count) {
		this.count = count;
	}

	public String getMarketPrice() {
		return marketPrice;
	}

	public void setMarketPrice(String marketPrice) {
		this.marketPrice = marketPrice;
	}

	public String getIntegral() {
		return integral;
	}

	public void setIntegral(String integral) {
		this.integral = integral;
	}

}
